package com.informationcatalyst.JavaIntroTasks;

public class CheckOddEven {
	public static void main(String[] args) {
		
		int number = 20;
		
		if (number % 2 > 0 ) {
			
			System.out.println("Odd Number");
		}
		else {
			
			System.out.println("Even Number");
		}
		System.out.println("BYE!");
//		System.exit(0);
	}
}
